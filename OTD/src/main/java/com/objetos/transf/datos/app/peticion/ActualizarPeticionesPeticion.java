package com.objetos.transf.datos.app.peticion;

public class ActualizarPeticionesPeticion {
	
	private Integer idPeticion;
	private Integer claveExterna;
	private Integer idMedioContacto;
	private String descricionMedioContacto;
	private String descripcionEntrega;
	
	
	public String getDescripcionEntrega() {
		return descripcionEntrega;
	}
	public void setDescripcionEntrega(String descripcionEntrega) {
		this.descripcionEntrega = descripcionEntrega;
	}
	public Integer getIdPeticion() {
		return idPeticion;
	}
	public void setIdPeticion(Integer idPeticion) {
		this.idPeticion = idPeticion;
	}
	public Integer getClaveExterna() {
		return claveExterna;
	}
	public void setClaveExterna(Integer claveExterna) {
		this.claveExterna = claveExterna;
	}
	public Integer getIdMedioContacto() {
		return idMedioContacto;
	}
	public void setIdMedioContacto(Integer idMedioContacto) {
		this.idMedioContacto = idMedioContacto;
	}
	public String getDescricionMedioContacto() {
		return descricionMedioContacto;
	}
	public void setDescricionMedioContacto(String descricionMedioContacto) {
		this.descricionMedioContacto = descricionMedioContacto;
	}
	
	
	

}
